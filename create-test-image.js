// Create a minimal PNG file manually (1x1 pixel)
function createMinimalPNG() {
  // This is a minimal 1x1 pixel PNG file
  const pngData = Buffer.from([
    0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a, // PNG signature
    0x00, 0x00, 0x00, 0x0d, // IHDR chunk length
    0x49, 0x48, 0x44, 0x52, // IHDR
    0x00, 0x00, 0x00, 0x01, // width: 1
    0x00, 0x00, 0x00, 0x01, // height: 1
    0x08, 0x02, 0x00, 0x00, 0x00, // bit depth, color type, compression, filter, interlace
    0x90, 0x77, 0x53, 0xde, // CRC
    0x00, 0x00, 0x00, 0x0c, // IDAT chunk length
    0x49, 0x44, 0x41, 0x54, // IDAT
    0x08, 0x99, 0x01, 0x01, 0x00, 0x00, 0xff, 0xff, 0x00, 0x00, 0x00, 0x02, 0x00, 0x01, // compressed data
    0x00, 0x00, 0x00, 0x00, // IEND chunk length
    0x49, 0x45, 0x4e, 0x44, // IEND
    0xae, 0x42, 0x60, 0x82, // CRC
  ]);

  const fs = require("fs");
  const path = require("path");
  const testImagePath = path.join(__dirname, "test-image.png");
  fs.writeFileSync(testImagePath, pngData);
  console.log("Created minimal test image:", testImagePath);
  return testImagePath;
}

// Export for testing
module.exports = { createMinimalPNG };

// Run if called directly
if (require.main === module) {
  createMinimalPNG();
}
